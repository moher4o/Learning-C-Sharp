        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DestroyPhoto(string photopath)
        {
            if (photopath == null)
            {
                TempData[ErrorMessageKey] = "Invalid photo details";
                return RedirectToAction(nameof(Photos));
            }

            var pathTokens = photopath.Split(new[] { "/" }, StringSplitOptions.RemoveEmptyEntries).ToList();
            var userId = pathTokens[pathTokens.Count - 2];
            var photoName = pathTokens[pathTokens.Count - 1];

            var user = await _userManager.GetUserAsync(User);
            if (user.Id != userId && !User.IsInRole(AdministratorRole))
            {
                return RedirectToAction("Login", "Account");
            }

            bool result = this.images.DeleteImage(userId, photoName);

            if (result)
            {
                bool isPhotoDeleted = this.photos.DeleteUserPhoto(photopath);
                TempData[SuccessMessageKey] = "Delete process interrupted";
                if (isPhotoDeleted)
                {
                    TempData[SuccessMessageKey] = "Delete successfull";
                }
            }

            return RedirectToAction(nameof(Photos));
        }

