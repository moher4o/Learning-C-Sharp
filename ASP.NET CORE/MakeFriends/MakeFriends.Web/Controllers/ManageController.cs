        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Photos(IEnumerable<IFormFile> newPhotos)
        {
            var user = await _userManager.GetUserAsync(User);
            if (user == null)
            {
                throw new ApplicationException($"Unable to load user with ID '{_userManager.GetUserId(User)}'.");
            }

            if (newPhotos == null)
            {
                TempData[ErrorMessageKey] = "You must select a photo";
                return View();
            }

            foreach (var photo in newPhotos)
            {
                if (!this.images.IsImagesLimitReached(user.Id))
                {

                    var fileName = this.images.AddImage(user.Id);
                    if (fileName != null)
                    {
                        var stream = photo.OpenReadStream();
                        var userId = user.Id;
                        var result = photos.ProcessPhoto(stream, userId, fileName);
                        if (result)
                        {
                            TempData[SuccessMessageKey] = "You successfuly upload your photo";
                        }
                        else
                        {
                            TempData[ErrorMessageKey] = "Unsuported image format";
                        }
                    }
                }
                else
                {
                    TempData[ErrorMessageKey] = "The number of photos reached its limit";
                }
            }

            var images = new PhotoViewModel();
            images.PhotoPaths = this.images.GetUserImagesPath(user.Id);
            images.SelectedPhotoPath = images.PhotoPaths.LastOrDefault();

            return View(images);
        }


